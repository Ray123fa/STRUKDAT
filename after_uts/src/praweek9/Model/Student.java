public class Student {
	private String nim;
	private String name;
	private int age;
	private String asal;

	public void setNim(String nim) {
		this.nim = nim;
	}

	public String getNim() {
		return nim;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getName() {
		return name;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public int getAge() {
		return age;
	}

	public void setAsal(String asal) {
		this.asal = asal;
	}

	public String getAsal() {
		return asal;
	}
}
